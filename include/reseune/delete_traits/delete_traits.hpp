#ifndef RESEUNE_DELETE_TRAITS_H
#define RESEUNE_DELETE_TRAITS_H

#include <inttypes.h>

// =====================================================================================================================
namespace reseune {
  // ===================================================================================================================
  class delete_traits {

  public:

    // =================================================================================================================
    template<typename T1>
    struct owner {
      static void destroy(T1&) {}
    };
    
    // =================================================================================================================
    template<typename T1>
    struct owner<T1*> {
      static void destroy(T1* p) { delete p; }
    };

    // =================================================================================================================
    template<typename T1>
    struct non_owner {
      static void destroy(T1&) {}
    };

    // =================================================================================================================
  };
  // ===================================================================================================================
}
// =====================================================================================================================
#endif
